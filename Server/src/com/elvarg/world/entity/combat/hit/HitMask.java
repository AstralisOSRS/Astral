package com.elvarg.world.entity.combat.hit;

/**
 * Represents a Hit mask type/color.
 * @author Gabriel Hannason
 */

public enum HitMask {
	
	BLUE,

	RED,

	GREEN,
	
	/*
	 * A light-yellow hitmask
	 */
	LIGHT_YELLOW,
	
	/*
	 * A dark purple hitmask
	 */
	DARK_PURPLE,
	
	/*
	 * A dark red hitmask
	 */
	DARK_RED,
	
	/*
	 * A dark crit hitmask
	 */
	DARK_CRIT,
	
	/*
	 * A green hitmask representing poison
	 */
	DARK_GREEN,

	/*
	 * No hitmask
	 */
	NONE,
	
	YELLOW;
	
}